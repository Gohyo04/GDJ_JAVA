package com.gohyo.study6.ex1;

public class WeatherDTO {

	private String city;
	private int ondo;
	private String wInfo;
	private int hum;
	
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public int getOndo() {
		return ondo;
	}
	public void setOndo(int ondo) {
		this.ondo = ondo;
	}
	public String getwInfo() {
		return wInfo;
	}
	public void setwInfo(String wInfo) {
		this.wInfo = wInfo;
	}
	public int getHum() {
		return hum;
	}
	public void setHum(int hum) {
		this.hum = hum;
	}
	
	
}
